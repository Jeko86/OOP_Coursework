    private void btnViewSelectedOrderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewSelectedOrderActionPerformed
        // Displays CustomerPreviosOrderLines
        
        //getting the selected row from the table
        int selectRow = tblOrders.getSelectedRow();
        
        //if nothing is selected (-1) will be displayed an error message
        if (selectRow == -1) 
        {
            lblErrorMessage.setForeground(Color.red);
            lblErrorMessage.setText("Error: Please Select an Order");
            lblImage.setIcon(new javax.swing.ImageIcon(getClass().getResource("/views/images/error.png")));
        }
        else//if the "selectedRow" is selected (rownumber) with 0 as column number (which holds the orderID)
        {
            int orderId = Integer.parseInt(String.valueOf(tblOrders.getValueAt(selectRow,0)));
            
            CustomerPreviosOrderLines previosOrderLines = new CustomerPreviosOrderLines(loggedInCustomer, orderId);
            this.dispose(); 
            previosOrderLines.setVisible(true);
        }
    }//GEN-LAST:event_btnViewSelectedOrderActionPerformed

