    public StaffPreviousOrders(Staff staff) {
        loggedInStaff = staff;
        
        //it set the background colour at the jframe page
        getContentPane().setBackground(Color.CYAN);
        
        initComponents();
        //It is called the customer from the Database to allow to display their order in the table using the HashMap
        DBManager db = new DBManager();
        HashMap<String, Customer> customers = db.loadCustomers();
        
        //the DefaultTableModel is outside the for loop to avoud its continuous loop
        DefaultTableModel previousOrdersModel =(DefaultTableModel)tblOrders.getModel();
        
        //for loop to naviga through the customers registered
        for(Map.Entry<String, Customer> customerEntry : customers.entrySet())
        {
            Customer customer = customerEntry.getValue();
            
            
        
            //the for loop will allow to display the logged customer's order data on the table 
            for(Map.Entry<Integer, Order> oEntry : customer.getOrders().entrySet())
            {
                Order order = oEntry.getValue();

                previousOrdersModel.addRow(new Object[]{
                    customer.getUsername(),
                    order.getOrderId(),
                    new SimpleDateFormat("dd/MM/yyyy HH:mm:ss").format(order.getOrderDate()),
                    "Â£" + String.format("%.2f", order.getOrderTotal())
                });
            }
        }
    }

